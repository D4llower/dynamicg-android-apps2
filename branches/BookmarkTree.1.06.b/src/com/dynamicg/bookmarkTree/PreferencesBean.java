package com.dynamicg.bookmarkTree;

public class PreferencesBean {

	public String folderSeparator;
	public String nodeConcatenation;
	public int disclaimerLastDisplayed;
	public int showDeleteIcon;
	public int optimisedLayout;
	public int listStyle;
	public int sortOption;
	public String getFolderSeparator() {
		return folderSeparator;
	}
	public void setFolderSeparator(String folderSeparator) {
		this.folderSeparator = folderSeparator;
	}
	public String getNodeConcatenation() {
		return nodeConcatenation;
	}
	public void setNodeConcatenation(String nodeConcatenation) {
		this.nodeConcatenation = nodeConcatenation;
	}
	public int getDisclaimerLastDisplayed() {
		return disclaimerLastDisplayed;
	}
	public void setDisclaimerLastDisplayed(int disclaimerLastDisplayed) {
		this.disclaimerLastDisplayed = disclaimerLastDisplayed;
	}
	public int getShowDeleteIcon() {
		return showDeleteIcon;
	}
	public void setShowDeleteIcon(int showDeleteIcon) {
		this.showDeleteIcon = showDeleteIcon;
	}
	public int getOptimisedLayout() {
		return optimisedLayout;
	}
	public void setOptimisedLayout(int optimisedLayout) {
		this.optimisedLayout = optimisedLayout;
	}
	public int getListStyle() {
		return listStyle;
	}
	public void setListStyle(int listStyle) {
		this.listStyle = listStyle;
	}
	public int getSortOption() {
		return sortOption;
	}
	public void setSortOption(int sortOption) {
		this.sortOption = sortOption;
	}
	
}
